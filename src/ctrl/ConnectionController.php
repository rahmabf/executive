<?php 
	require_once('src/ctrl/Controller.php');
	require_once('src/mdl/User.php');
	session_start();

	class ConnectionController extends Controller{

		public function connect(){
			if(isset ($_SESSION['user'])){
				$this->redirect('executive','index','dash');


			}
			else if (isset($_POST['email']) && isset($_POST['psw'])) {
				$user = new User(); 		
				if ($user->connection($_POST['email'],$_POST['psw'])) {
					$_SESSION['user']=$user;
					$this->redirect('executive','index','dash');

					

				}
				else {
					echo('error');
				}
					
			

			}
			else {
					require_once('src/view/connection.php');
				}

		}
		public function disconnect(){
			$_SESSION=array();
			session_destroy();
							$this->redirect('executive','index','connect');


	}

		public function stat(){
			require_once('src/view/accueil.php');

		}
}











































































































































































































































































































































































































